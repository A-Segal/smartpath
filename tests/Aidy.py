##מערכת ניהול משימות (Full Stack) | Angular, Nodצe.js, SQL Server

#פיתוח מערכת ניהול משימות ומשתמשים מקצה לקצה הכוללת מנגנון הרשאות, ניהול משתמשים, Dashboard וצ'אט בזמן אמת.

 #המערכת פותחה ב-Angular תוך שימוש ב-RxJS, Angular Material, Signals, Reactive Forms, Directives ו-Pipes מותאמים אישית.

  #בצד השרת פותח REST API ב-Node.js ו-Express עם חיבור ל-SQL Server באמצעות Stored Procedures ו-Triggers.

  #בנוסף מומשה תקשורת WebSocket לשליחת הודעות והתראות בזמן אמת על משימות.

    #הפרויקט# כלל עבודה עם ארכיטקטורת Services, ניהול State, אבטחת מסכים והרשאות משתמשים.